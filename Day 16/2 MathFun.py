#Using math module
import math
print(math.sqrt(144))
print(math.ceil(4.2))
print(math.floor(4.2))
print(math.factorial(6))
print(math.gcd(12,146))
print(math.log(20)) #Log( with base e (normal log))
print(math.log10(20)) #Log( with base anything )

#Trigonometric functions
print(math.sin(math.radians(90)))
print(math.cos(math.radians(90)))
print(math.tan(math.radians(90)))

#Math constants
print(math.pi)
print(math.e)