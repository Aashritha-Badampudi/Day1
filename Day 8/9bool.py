#XOR
print(True^True^True)      #3 (odd)  True
print(True^True^False)     #2 (even) False  
print(True^False^True)     #2 (even) False  
print(True^False^False)    #1 (odd)	 True  
print(False^True^True)     #2 (even) False   
print(False^True^False)    #1 (odd)	 True  
print(False^False^True)    #1 (odd)  True
print(False^False^False)   #0 (even) False
