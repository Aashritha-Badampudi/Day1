#A list is a sequence data type used to store multiple values in one variable.

# Key Characteristics of Lists

# 1️⃣ Ordered
# Items keep the same order as you add them

# 2️⃣ Mutable (Changeable)
# You can modify items after creation

# 3️⃣ Allows duplicates
# Same value can appear more than once

# 4️⃣ Can store mixed data types
# int, float, string, boolean, even other lists
